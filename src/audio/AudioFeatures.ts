/**
 * AudioFeatures — ビジュアライザのプラグインに毎フレーム渡す「音の数値セット」。
 *
 * 設計方針（詳しくは ../../docs/architecture.md）:
 *  - スカラー値は原則 **0〜1 に正規化** 済み。プラグインは掛け算するだけで描ける
 *    （例: `height = features.bass * canvas.height`）。
 *  - 生配列（spectrum / waveform）も同梱。バーや波形など「全ビン使う」描画はこちらを使う。
 *  - 「なめらかな値」と「鋭い瞬間値」を分けて持つ
 *    （rms / bass などは平滑化済み、peak / impulse は平滑化なしの鋭い値）。
 *  - Hz だけは例外で生値（0〜1 にすると意味が消えるため）。
 */
export interface AudioFeatures {
  // ── 生データ（"全ビン使う"系の描画用）──────────────────────────────

  /** 周波数ビン配列。長さ = fftSize/2（既定1024）。各要素 0〜255（低音→高音）。 */
  spectrum: Uint8Array;
  /** 波形（時間領域）。長さ = fftSize。各要素 −1〜1 付近。オシロ風の描画に。 */
  waveform: Float32Array;

  // ── 音量系（0〜1）──────────────────────────────────────────────

  /** 全体の音量感（RMS）。平滑化＆オートゲイン済み。映像全体のスケールに。 */
  rms: number;
  /** その瞬間の最大振れ幅。平滑化なしの鋭い値。急な大音量でのフラッシュに。 */
  peak: number;

  // ── 音域別エネルギー（0〜1、平滑化済み）────────────────────────────

  /** 低音（既定 20〜150Hz）。ベース・バスドラム帯。 */
  bass: number;
  /** 中音（既定 150〜2000Hz）。 */
  mid: number;
  /** 高音（既定 2k〜16kHz）。シンバル・ハイハット帯。 */
  treble: number;
  /** 任意分割版（既定8分割、対数間隔）。汎用の多バンド描画に。各 0〜1。 */
  bands: number[];

  // ── 質感系（0〜1、平滑化済み）──────────────────────────────────

  /** 明るさ（スペクトラル・セントロイドの正規化）。高音が多いほど大。色相などに。 */
  brightness: number;
  /** 変化量（スペクトラル・フラックス）。音の動きの激しさ。平滑化版。 */
  flux: number;
  /** 上の広がり（ロールオフ。エネルギーの大半が収まる高さの割合）。 */
  rolloff: number;
  /** ノイズ寄り(1)⇄澄んだ音寄り(0)（スペクトラル・フラットネス）。 */
  flatness: number;
  /** ザラつき・高音っぽさ（ゼロクロス率）。 */
  noisiness: number;

  // ── 音程系 ───────────────────────────────────────────────────

  /**
   * 12音名の含有量（0=C, 1=C#, … 11=B）。各 0〜1。和音でも動く。
   * 大FFTのスペクトルからピークのみ集計し、ホワイトニング（局所平均を引く）＋放物線補間で精密化。
   */
  chroma: number[];
  /** いちばん強い音の高さ（Hz）。※これだけ正規化しない生値。 */
  loudestHz: number;

  // ── 調性（chroma を五度圏で1方向にまとめた信号）──────────────────

  /** 調性の向き。0〜1 の循環値（一周＝1）。「いまどの調性が優勢か」。色相などに。 */
  tonalAngle: number;
  /** 調性のはっきり具合。0〜1（1=明確な和音/単音、0=調性なし＝ノイズ/打楽器）。 */
  tonalStrength: number;
  /** 調性ベクトルの成分（−1〜1）。継ぎ目を気にせず使える。tonalX²+tonalY² = tonalStrength²、角度 = tonalAngle。 */
  tonalX: number;
  tonalY: number;

  // ── 鋭い反応用（平滑化しない瞬間値）─────────────────────────────

  /** 立ち上がりの鋭さ（オートゲイン済みフラックスの平滑化なし版）。拍の種。0〜1。 */
  impulse: number;
  /** 拍が来た瞬間だけ true（適応しきい値＝直近包絡の平均+k×標準偏差＋不応期による検出）。 */
  beat: boolean;

  // ── リズム / テンポ（Tier B：数秒の蓄積が必要）──────────────────

  /** 推定テンポ（BPM）。0=未確定。簡易推定なので2倍/半分の誤りはありうる。 */
  bpm: number;
  /** 拍間の位相 0〜1（拍で0に戻り、次の拍へ向けて増える）。テンポ同期の脈動に。 */
  beatPhase: number;
  /** 低域オンセット強度 0〜1（バスドラム帯の「立ち上がり」）。 */
  onsetLow: number;
  /** 中域オンセット強度 0〜1（スネア帯）。 */
  onsetMid: number;
  /** 高域オンセット強度 0〜1（ハイハット/シンバル帯）。 */
  onsetHigh: number;
  /** 低域を叩いた瞬間だけ true（≒キック）。帯域別オンセットの適応しきい値検出。 */
  kick: boolean;
  /** 中域を叩いた瞬間だけ true（≒スネア）。帯域別オンセットの適応しきい値検出。 */
  snare: boolean;
  /** 高域を叩いた瞬間だけ true（≒ハイハット）。帯域別オンセットの適応しきい値検出。 */
  hat: boolean;

  // ── ダイナミクス ──────────────────────────────────────────────

  /** 音量の増減 −1〜1（+ = 大きくなりつつある / − = 小さく）。ビルドの判定に。 */
  energyDelta: number;
  /** 急な大音量の立ち上がり（ドロップ/衝撃）の瞬間だけ true。 */
  drop: boolean;
  /** ほぼ無音のとき true（絶対音量がしきい値未満）。アイドル制御に。 */
  silence: boolean;

  // ── ステレオ ──────────────────────────────────────────────────

  /** 左右バランス −1（左）〜+1（右）。0=中央。 */
  pan: number;
  /** ステレオの広がり 0（モノ）〜1（広い）。L/R の相関から算出。 */
  stereoWidth: number;

  // ── キー（調）推定（Tier B：accumulated chroma）──────────────────

  /** 主音 0=C 〜 11=B（−1=未確定）。 */
  keyIndex: number;
  /** 長調=true / 短調=false。 */
  keyIsMajor: boolean;
  /** キー推定の確信度 0〜1。 */
  keyConfidence: number;

  // ── メタ ─────────────────────────────────────────────────────

  /** サンプリングレート（Hz）。ビン→周波数変換に使う（ナイキスト周波数 = sampleRate/2）。 */
  sampleRate: number;
  /** AudioContext 基準の経過秒。アニメーションの位相計算に便利。 */
  time: number;
}
